package org.example;

/**
 * Интерфейс, който дефинира метод за получаване на обновления от кролера.
 * Използва се за наблюдение на състоянието на кролера и за получаване на съобщения,
 * свързани с процеса на обхождане.
 */
public interface CrawlerObserver {

    /**
     * Метод за актуализиране на наблюдателя със съобщение.
     *
     * @param message Съобщение, което съдържа информация за текущото състояние на кролера.
     */
    void update(String message);
}
